    /**
     * Complete the puzzle.
     *
     * @return self
     */
    public function __invoke(): self
    {
        $this->part1();
        $this->part2();
        return $this;
    }

    /**
     * Complete part one of the puzzle.
     *
     * @return void
     */
    public function part1()
    {
        $this->start();

        $lines = collect(explode("\n", $this->getInput()));

        $x = 1;
        $trees = 0;
        $move = 3;

        foreach ($lines as $line) {
            while (strlen($line) < $x) {
                $line.= $line;
            }

            if (substr($line, $x - 1, 1) === '#') $trees++;
            $x += $move;
        }

        $this->finish();

        $this->addPart([
            'table' => [
                'headings' => ['Part 1: Answer'],
                'data' => [[$trees.' ðŸŽ„']],
            ],
            'time' => $this->getTime(),
        ]);
    }

    /**
     * Complete part two of the puzzle.
     *
     * @return void
     */
    public function part2()
    {
        $this->start();

        $lines = collect(explode("\n", $this->getInput()));

        $slopes = [
            ['move' => 1],
            ['move' => 3],
            ['move' => 5],
            ['move' => 7],
            ['move' => 1, 'skip' => true],
        ];

        foreach ($slopes as $slopei => $slope) {
            $x = 1;
            $trees = 0;

            foreach ($lines as $i => $line) {
                if ($slope['skip'] ?? false) {
                    $even = $i % 2 == 0;
                    if ($slope['skip'] && !$even) continue;
                }

                while (strlen($line) < $x) {
                    $line.= $line;
                }

                if (substr($line, $x - 1, 1) === '#') $trees++;
                $x += $slope['move'];
            }

            $slopes[$slopei]['trees'] = $trees;
        }

        $this->finish();

        $this->addPart([
            'table' => [
                'headings' => ['Part 2: Answer'],
                'data' => [[array_product(collect($slopes)->pluck('trees')->toArray())]],
            ],
            'time' => $this->getTime(),
        ]);
    }

